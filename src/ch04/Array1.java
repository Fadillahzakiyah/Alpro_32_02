package ch04;

public class Array1 {
    public static void main(String[] args) {
        int[] nilai = {82, 12, 41, 38, 19, 26, 9, 48, 20, 55, 8, 32, 3};
        System.out.println("Menampilkan semua nilai");
        for(int a = 0; a < 13; a++){
            System.out.print(nilai[a]+ " ");
        }
    }
}
